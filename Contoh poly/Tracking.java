
public class Tracking {

    public void lacak(String a, String b, String c, String d) {
        System.out.println("Target berada di 4 kota");
        System.out.println(a + " " + b + " " + c + " " + d);
    }

    public void lacak(String a, String b, String c) {
        System.out.println("Target berada di 3 kota");
        System.out.println(a + " " + b + " " + c);
    }

    public void lacak(String a, String b) {
        System.out.println("Target berada di 2 kota");
        System.out.println(a + " " + b);
    }

    public void lacak(String a) {
        System.out.println("Target berada di 1 kota");
        System.out.println(a);
    }
}
